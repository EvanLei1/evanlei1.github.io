          <h3 id="SCT">Sonoma County Transit</h3>
          <p>Orion, ENC, and BYD buses make up the fleet. It consists of 14 Orion VII NG CNG buses made in 2010 and
            2012, 17 ENC Axess CNG 40' from 2014 to 2019,
            7 ENC EZ Rider II BRT CNG 32' buses made in 2015, 2017, and 2019, and 11 BYD buses of various
            length made from 2018 to 2026. The last remaining Orion V CNG buses retired in late 2024.
            Two buses (235-236) were ex-Valley Metro 5000 series, the last New Flyer C40LFRs delivered in Phoenix in
            2013.
          </p>
          <h3 id="CityBus">Santa Rosa CityBus</h3>
          <p>Most of the buses were New Flyer, including 7 2011 New Flyer XDE40 buses. Four 2018 ENC Axess BRT 40'
            were used in service.
            Two buses were seen on Pantograph as 2024 models, but the model is unknown. In October 2025, 6 Gillig
            hybrid buses were acquired from the Golden Gate Transit (24918-24923), replacing the Proterra buses and
            the last remaining 2008 hybrid.
            In January 2026, the 2025 Gillig electric vehicles entered service, replacing the oldest units.
          </p>
